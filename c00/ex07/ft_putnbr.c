#include <unistd.h>

void	ft_putnbr(int nbr)
{
	int		i;
	char	str[12];

	i = 0;
	if (nbr >= -2147483647 && nbr <= 2147483647)
	{
		if (nbr < 0)
		{
			nbr = -nbr;
			write (1, "-", 1);
		}
		while (nbr > 0 && nbr <= 2147483647)
		{
			str[i] = nbr % 10 + '0';
			nbr /= 10;
			i++;
		}
		str[i] = '\0';
		while (str[--i] != '\0')
		{
			write (1, &str[i], 1);
		}
		write (1, "\n", 1);
	}
}
